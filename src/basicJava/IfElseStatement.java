package basicJava;

public class IfElseStatement {
    public static void main(String[] args) {
        int coco=20;
        int lemon=15;
        String product1="coco";
        String product2="lemon";


        if(coco<30){
            System.out.println(product1+coco);

        }else{
            System.out.println(product2+lemon);
        }
    }
}
